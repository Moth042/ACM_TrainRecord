void moth()
{
    int n;
    cin >> n;
    vector<ll> a(n + 1), b(n + 1);
    for (int i = 1; i <= n; i++) cin >> a[i];
    for (int i = 1; i <= n; i++) cin >> b[i];
    ll f0 = 0, f1 = -1e18;
    for (int i = 1; i <= n; i++)
    {
        ll x = a[i], k = b[i];
        ll g0, g1;
        if (x <= 0) g0 = max(f0, f1), g1 = -1e18;
        else
        {
            g0 = max(f0 + k / 2 * x, f1 + (k - 1) / 2 * x);
            g1 = max(f0 + (k + 1) / 2 * x, f1 + k / 2 * x);
        }
        f0 = g0, f1 = g1;
    }
    cout << max(f0, f1) << '\n';
}
